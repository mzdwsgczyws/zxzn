function padYear(y) {
  if (SOLAR_TERMS_BY_YEAR[y]) return y
  if (y < 2024) return 2024
  if (y > 2030) return 2030
  return y
}

function compareMD(m1, d1, m2, d2) {
  if (m1 !== m2) return m1 - m2
  return d1 - d2
}

/**
 * @param {Date} date
 * @returns {{ jieqi: string, nextJieqi: string, jianchu: string, yi: string[], ji: string[], summary: string }}
 */
function getHuangdaoPackage(date, gzStr) {
  const y = date.getFullYear()
  const m = date.getMonth() + 1
  const d = date.getDate()
  const yy = padYear(y)
  const terms = SOLAR_TERMS_BY_YEAR[yy] || SOLAR_TERMS_BY_YEAR[2025]

  let jieqi = terms[0][2]
  for (let i = 0; i < terms.length; i++) {
    const [tm, td, name] = terms[i]
    if (compareMD(m, d, tm, td) >= 0) jieqi = name
    else break
  }

  const idx = terms.findIndex(([tm, td]) => compareMD(m, d, tm, td) < 0)
  const nextJieqi = idx >= 0 ? terms[idx][2] : '小寒'

  let h = 0
  const g = String(gzStr || '')
  for (let i = 0; i < g.length; i++) h = (h + g.charCodeAt(i)) % 997
  const jcIndex = (m * 31 + d * 3 + h) % 12
  const jianchu = JIANCHU[jcIndex]
  const yi = YI_POOL[jianchu] || YI_POOL['平']
  const ji = JI_POOL[jianchu] || JI_POOL['平']

  const summary = `节气在「${jieqi}」前后；日值「${jianchu}」日。宜：${yi.slice(0, 2).join('、')}；忌：${ji.slice(0, 2).join('、')}（民俗参考）。`

  return { jieqi, nextJieqi, jianchu, yi, ji, summary }
}

