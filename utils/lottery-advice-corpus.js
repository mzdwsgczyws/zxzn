/**
 * 按上下文取出应注入候选池的条目 { cat, text, w }
 * w 为重复权重
 */
function collectCorpusEntries(ctx) {
  const { lotId = 0, wxDay = '', tier = '中', typeId = null, userTone: toneIn } = ctx
  const personality = ctx.personality
  const profile = ctx.profile
  const recentState = ctx.recentState

  const out = []
  const add = (cat, text, w) => {
    for (let i = 0; i < w; i++) out.push({ cat, text })
  }

  const lid = Math.max(0, Math.min(63, Number(lotId) || 0))
  const userTone = toneIn != null ? toneIn : resolveUserTone({ personality, profile, recentState })
  getLotPushes(lid, userTone).forEach((p) => {
    add('签象', p.text, p.w)
  })

  const wx = BY_WX[wxDay]
  if (wx && wx.length) {
    const seed = (lid * 7 + (wxDay || '').length) % wx.length
    for (let k = 0; k < 4; k++) {
      add('五行', wx[(seed + k) % wx.length], 2)
    }
  }

  const tr = BY_TIER[tier] || BY_TIER['中']
  if (tr && tr.length) {
    const seed2 = (lid + (tier || '').length) % tr.length
    for (let k = 0; k < 3; k++) {
      add('等第', tr[(seed2 + k) % tr.length], 2)
    }
  }

  if (typeId != null && typeId >= 0 && typeId <= 15) {
    const arr = BY_TYPE_ID[typeId]
    if (arr) {
      arr.forEach((line) => add('道性', line, 3))
    }
  }

  return out
}

